# Get the numbers through user input
number_1 = int(input("Enter the First Number  : "))
number_2 = int(input("Enter the Second Number : "))

# Print the addition of the input numbers
print("Addition of input numbers : ", number_1 + number_2)

# Print the difference between the input numbers
print("Difference between input numbers : ", number_1 - number_2)

# Print the multiplication of the input numbers
print("Multiplication of input numbers : ", number_1 * number_2)

# Print the result of division between input numbers
print("Result of Division of input numbers : ", number_1 / number_2)

# Print the Modulus of input numbers
print("Modulus of input numbers : ", number_1 % number_2)

# Print First Number to the Power of Second Number
print("Result of First Number to the Power of Second Number : ", number_1 ** number_2)

# Print the result of division between input numbers, round down to the nearest whole number
print("Result of Division of input numbers, round down to nearest whole number : ", number_1 // number_2)
